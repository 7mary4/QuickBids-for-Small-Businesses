            <h4>PPE Steps</h4>
            <ol>
            <li>Login to <a href="http://www.ppereports.com">Past Performance Reports</a>, enter contact info, search for business</li>
            <li>Choose the primary company name and the alternate name (trade style or dba) that you want to be shown on the PPE report</li>
            <li>Choose the type of report required:  PPE or PPE/Supplier Qualifier.  Generally, federal agencies require just the PPE report, state/county/US Army require PPE/Supplier Qualifier</li>
            <li>Enter 4-20 customer references who will be contacted to fill out a short 10-question survey (You need first/last name, company name, phone number, email address (this info is kept confidential by D&amp;B)</li>
            <li>Enter the name and e-mail information for the recipient that should receive the completed PPE report. Please note that you will also receive a copy of the report</li>
            <li>Confirm at the bottom of the page, you are initiating the order process and agreeing to the order terms and conditions</li>
            <li>Enter credit card information to complete the transaction</li>
            </ol>
